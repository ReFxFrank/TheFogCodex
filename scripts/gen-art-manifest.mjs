// Scans public/images/{perks,characters} for real art and writes a manifest
// that the app reads to decide whether to show a real image or the generated
// placeholder. Runs automatically on postinstall / predev / prebuild.
//
// Drop <slug>.png (or .jpg/.webp/.avif) into the folders and rebuild — no code
// changes needed. The manifest itself is gitignored, so the art (which you
// source yourself) never has to be committed.

import { readdirSync, writeFileSync, existsSync, mkdirSync } from "node:fs";
import { join, dirname } from "node:path";
import { fileURLToPath } from "node:url";

const root = join(dirname(fileURLToPath(import.meta.url)), "..");
const EXTS = new Set([".png", ".jpg", ".jpeg", ".webp", ".avif"]);

function scan(folder, urlBase) {
  const dir = join(root, "public", "images", folder);
  const map = {};
  if (existsSync(dir)) {
    for (const file of readdirSync(dir)) {
      const dot = file.lastIndexOf(".");
      if (dot < 1) continue; // skip .gitkeep and dotfiles
      const slug = file.slice(0, dot);
      const ext = file.slice(dot).toLowerCase();
      if (!EXTS.has(ext)) continue;
      map[slug] = `${urlBase}/${file}`;
    }
  }
  return map;
}

const perks = scan("perks", "/images/perks");
const characters = scan("characters", "/images/characters");

const body = `// AUTO-GENERATED by scripts/gen-art-manifest.mjs — do not edit by hand.
// Maps entity slug -> real image path for any art present under public/images.
export const PERK_ART: Record<string, string> = ${JSON.stringify(perks, null, 2)};
export const CHARACTER_ART: Record<string, string> = ${JSON.stringify(characters, null, 2)};
`;

const outDir = join(root, "src", "data");
mkdirSync(outDir, { recursive: true });
writeFileSync(join(outDir, "art-manifest.ts"), body);

console.log(
  `[art] manifest written: ${Object.keys(perks).length} perk image(s), ${Object.keys(characters).length} character image(s)`,
);
